export function createBaseHand(ownerId: BattleSide): BattleCardModel[] {
  return BASE_HAND_LIBRARY.map((template) => ({
    ...template,
    id: `${ownerId}-${template.templateId}`,
    ownerId,
    zone: "hand",
    isCrushed: false,
  }));
}

export function cloneEquipments(list: EquipmentModel[]): EquipmentModel[] {
  return list.map((item) => ({ ...item }));
}
